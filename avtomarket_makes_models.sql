CREATE TABLE vehicle_classes (
  id INT NOT NULL AUTO_INCREMENT,
  code VARCHAR(64) NOT NULL,
  label VARCHAR(255) NOT NULL,
  PRIMARY KEY (id),
  UNIQUE KEY uq_vehicle_classes_code (code)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
CREATE TABLE vehicle_makes (
  id INT NOT NULL AUTO_INCREMENT,
  class_id INT NOT NULL,
  code VARCHAR(64) NOT NULL,
  label VARCHAR(255) NOT NULL,
  PRIMARY KEY (id),
  KEY idx_makes_class (class_id),
  CONSTRAINT fk_makes_class FOREIGN KEY (class_id)
    REFERENCES vehicle_classes (id)
    ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
CREATE TABLE vehicle_models (
  id INT NOT NULL AUTO_INCREMENT,
  make_id INT NOT NULL,
  code VARCHAR(64) NOT NULL,
  label VARCHAR(255) NOT NULL,
  PRIMARY KEY (id),
  KEY idx_models_make (make_id),
  CONSTRAINT fk_models_make FOREIGN KEY (make_id)
    REFERENCES vehicle_makes (id)
    ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

